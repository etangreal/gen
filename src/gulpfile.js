var gulp 				= require('gulp');
var gulpFilter 	= require('gulp-filter');
var sass        = require('gulp-sass');

// ------------------------------------------------------------------------------------------------
// DECLARATIONS
// ------------------------------------------------------------------------------------------------

var ROOT 			= './';
var SRC 			= './';
var TEST      = '../tests/';
var ALL 			= '**/*';

var FILES = [
		// SRC,
    SRC + ALL,
		TEST,
	];

// ------------------------------------------------------------------------------------------------
// TASK: SASS
// ------------------------------------------------------------------------------------------------

gulp.task('sass', function () {
    gulp.src('./public/*.scss')
        .pipe(sass())
        .pipe(gulp.dest('./public'))
});

// ------------------------------------------------------------------------------------------------
// GULP: DEFAULT
// ------------------------------------------------------------------------------------------------

gulp.task('default', function () {
	gulp.watch(['./public/*.scss'], ['sass'])
});

