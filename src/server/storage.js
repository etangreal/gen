
// ------------------------------------------------------------------------------------------------
// STORAGE (Server)
// ------------------------------------------------------------------------------------------------


var self = module.exports = {

	store: {}, 

	// ----------------------------------------------------------------------------------------

	add: function(token, name) {
		self.store[token] = name;
	},

	// ----------------------------------------------------------------------------------------

	find: function(token) {
		return self.store[token];
	},

	// ----------------------------------------------------------------------------------------

	exists: function(token) {
		return (token in self.store);
	}

	// ----------------------------------------------------------------------------------------

};//module.exports

